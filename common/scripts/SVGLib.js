	/**
	 * Inserts all icons into marked containers on a page
	 */
	ns.insertIcons = function insertIcons()
	{
		var elements = document.getElementsByClassName(ICON_CONTAINER_CLASS);
		for (element of elements)
		{
			const iconName = element.getAttribute(ICON_TYPE_ATTRIBUTE);
			if (!ns.Icons[iconName])
			{
				element.innerHTML = `<span class="${ICON_CLASS} ${ICON_UNLOADED_CLASS}">${element.innerHTML}</span>`;
				continue;
			}

			const title = element.innerText;
			element.innerHTML = "";
			element.appendChild(
				ns.createIcon(
					ns.Icons[iconName],
					title,
					element.getAttribute(ICON_DESCRIPTION_ATTRIBUTE),
					element.getAttribute(ICON_CLASSES_ATTRIBUTE),
					element.getAttribute(ICON_STYLE_ATTRIBUTE),
				)
			);
		};
	};

	/**
	 * Creates and returns an icon to spec
	 */
	ns.createIcon = function createIcon(iconDef, title, desc, classes, style)
	{
		var iconEl = createElement(
			ns.ElementTypes.svg,
			{
				"viewBox": iconDef.viewBox,
				"class": `${ICON_CLASS} ${classes}`,
				"aria-role": "img",
				"style": style || ""
			},
			ICON_CITATION
		);

		createChildElement(
			iconEl,
			ns.ElementTypes.title,
			{},
			title || iconDef.title
		);
		if (desc)
		{
			createChildElement(
				iconEl,
				ns.ElementTypes.desc,
				{},
				desc
			);
		}

		createChildElement(
			iconEl,
			ns.ElementTypes.path,
			{
				"d": iconDef.d
			}
		);

		return iconEl;
	};

	ns.IconEl = class IconEl extends HTMLElement
	{
		//#region staticProperties
		static observedAttributes = [];
		static instanceCount = 0;
		static instanceMap = {};
		//#endregion

		//#region instance properties
		instanceId;
		iconObj;
		titleText;
		isInitialized;

		//#region element properties
		//#endregion
		//#endregion

		constructor()
		{
			super();
			this.instanceId = IconEl.instanceCount++;
			IconEl.instanceMap[this.instanceId] = this;
		}

		get idKey()
		{
			return `gw-icon-${this.instanceId}`;
		}

		//#region HTMLElement implementation
		connectedCallback()
		{
			if (this.isInitialized) { return; }

			this.iconObj = ns.Icons[this.getAttribute("iconKey")];

			this.titleText = this.getAttribute("title");
			if (this.hasAttribute("titleId"))
			{
				const titleEl = document.getElementById(this.getAttribute("titleId"));
				this.titleText = titleEl.innerText;
				titleEl.remove();
			}

			this.iconDescription = this.getAttribute("description");
			this.iconClasses = this.getAttribute("iconClasses");
			this.iconStyle = this.getAttribute("iconStyle");

			this.renderContent();
			this.isInitialized = true;
		}
		//#endregion

		renderContent()
		{
			if (!this.iconObj)
			{
				debugger;
				return;
			}

			//Markup
			this.appendChild(
				ns.createIcon(
					this.iconObj,
					this.titleText,
					this.iconDescription,
					this.iconClasses,
					this.iconStyle,
				)
			);

			//element properties
		}
	};
	customElements.define("gw-icon", ns.IconEl);
	//#endregion
